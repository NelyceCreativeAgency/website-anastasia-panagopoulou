/* =============================================================
   Inject the homepage testimonial marquee from content/testimonials/*.md
   (CMS-managed via Decap, /admin) into index.html, between the
   TESTIMONIALS:START / TESTIMONIALS:END markers.

   The marquee has two rows for visual variety - forward (testimonials
   in order) and reverse (same set, reversed) - each seeded with a
   single .tmarquee__set; js/main.js clones that set at runtime to
   fill the row width for the seamless scroll loop, so this script
   only ever needs to emit one set per row.

   Run via `npm run build:testimonials` (also wired into `npm run
   build`, so every Vercel deploy stays in sync with the CMS).
   ============================================================= */
const fs = require("fs");
const path = require("path");
const matter = require("gray-matter");

const ROOT = path.join(__dirname, "..");
const CONTENT_DIR = path.join(ROOT, "content", "testimonials");
const INDEX_HTML = path.join(ROOT, "index.html");
const START_MARKER = "<!-- TESTIMONIALS:START — generated by scripts/build-testimonials.js from content/testimonials/*.md, do not hand-edit -->";
const END_MARKER = "<!-- TESTIMONIALS:END -->";

function escapeHtml(str) {
  return String(str).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}
function escapeAttr(str) {
  return String(str).replace(/&/g, "&amp;").replace(/"/g, "&quot;");
}

function loadTestimonials() {
  if (!fs.existsSync(CONTENT_DIR)) return [];
  const files = fs.readdirSync(CONTENT_DIR).filter((f) => f.endsWith(".md"));
  const items = files.map((file) => {
    const raw = fs.readFileSync(path.join(CONTENT_DIR, file), "utf8");
    const { data } = matter(raw);
    return {
      order: typeof data.order === "number" ? data.order : 999,
      file,
      name: data.name || "",
      role_el: data.role_el || "",
      role_en: data.role_en || "",
      avatar: data.avatar || "",
      text_el: data.text_el || "",
      text_en: data.text_en || "",
    };
  });
  items.sort((a, b) => a.order - b.order || a.file.localeCompare(b.file));
  return items;
}

function card(t) {
  return `            <article class="tmcard">
              <p class="tmcard__text"><span data-lang-el>${escapeHtml(t.text_el)}</span><span data-lang-en>${escapeHtml(t.text_en)}</span></p>
              <div class="tmcard__person"><img class="tmcard__avatar" src="${escapeAttr(t.avatar)}" alt="" loading="lazy" width="52" height="52"><div><div class="tmcard__name">${escapeHtml(t.name)}</div><div class="tmcard__role"><span data-lang-el>${escapeHtml(t.role_el)}</span><span data-lang-en>${escapeHtml(t.role_en)}</span></div></div></div>
            </article>`;
}

function row(className, items) {
  return `      <div class="tmarquee__row ${className}" aria-hidden="true">
        <div class="tmarquee__track">
          <div class="tmarquee__set">
${items.map(card).join("\n")}
          </div>
        </div>
      </div>`;
}

function build() {
  const testimonials = loadTestimonials();
  if (!testimonials.length) {
    console.warn("[build-testimonials] No entries found in content/testimonials — skipping.");
    return;
  }

  const html = fs.readFileSync(INDEX_HTML, "utf8");
  const startIdx = html.indexOf(START_MARKER);
  const endIdx = html.indexOf(END_MARKER);
  if (startIdx === -1 || endIdx === -1) {
    console.warn("[build-testimonials] TESTIMONIALS markers not found in index.html — skipping.");
    return;
  }

  // Row 2 reuses the same set, rotated by half so the two rows never open
  // on the same card (a plain .reverse() would put the last card first,
  // which reads as a mirror rather than a genuinely different lineup).
  const offset = Math.floor(testimonials.length / 2);
  const rotated = [...testimonials.slice(offset), ...testimonials.slice(0, offset)];

  const forward = row("tmarquee__row--fwd", testimonials);
  const reverse = row("tmarquee__row--rev", rotated);
  const block = `${START_MARKER}\n      <!-- Row 1, scrolls left -->\n${forward}\n\n      <!-- Row 2, scrolls right (rotated order) -->\n${reverse}\n      ${END_MARKER}`;

  const newHtml = html.slice(0, startIdx) + block + html.slice(endIdx + END_MARKER.length);
  fs.writeFileSync(INDEX_HTML, newHtml, "utf8");
  console.log(`[build-testimonials] Injected ${testimonials.length} testimonial(s) into index.html`);
}

build();
